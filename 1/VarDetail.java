public class VarDetail{
	//编写一个main（）函数
	public static void main(String[] args) {
		char c1 = 'a';
		char c2 = '\t';
		char c3 = '韩';
		char c4 = 97;
		char c5 = 'b' + 5;
		System.out.println(c1);
		System.out.println(c2);
		System.out.println((int)c3);
		System.out.println(c4);
		System.out.println('a' + 1 );
		System.out.println(c5);
	}
}