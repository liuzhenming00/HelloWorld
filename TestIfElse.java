public class TestIfElse{
	public static void main(String[] args) {
		int i=20;
		if(i==10) {
			System.out.println("i的值为10");
		}
	
	
		else if(i==20) {
			System.out.println("i的值为20");
		}
		else if(i==30) {
			System.out.println("i的值为30");
		}
		else
		{
			System.out.println("这是else语句");
		}
	}
		
}